    private void completar(){
        jTblFuncionarios.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent evt) {


                JTable table = (JTable)evt.getSource();
                Point point = evt.getPoint();
                int row = table.rowAtPoint(point);
                if (evt.getClickCount() == 2  && table.getSelectedRow() != -1) {

                    jTFDocumento.setText(jTblFuncionarios.getValueAt(row, 1).toString());
                    jTFNombres.setText(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 2).toString());
                    jTFApellidos.setText(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 3).toString());
                    jTADireccion.setText(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 5).toString());
                    jFFTelefono.setText(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 6).toString());
                    JDChFechaNac.setDate(java.sql.Date.valueOf(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 7).toString()));
                    jCBTipoDocumento.setSelectedItem(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 8).toString());
                    JCBEstadoCivil.setSelectedItem(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 9).toString());
                    jCBSexo.setSelectedItem(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 4).toString());

                }
            }
        });
    }
    private void limpiar(){
        jTFDocumento.setText("");
        jTFNombres.setText("");
        jTFApellidos.setText("");
        jFFTelefono.setText("");
        jTADireccion.setText("");
        jCBTipoDocumento.setSelectedIndex(0);
        JCBEstadoCivil.setSelectedIndex(0);
        jCBSexo.setSelectedIndex(0);
        JDChFechaNac.setCalendar(null);
    }
    
    private void eliminar(){
        String documento = (String)jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 1);
        funcionarioCtrl.eliminar(documento);
    }
    
    private boolean guardar(){
        Funcionario funcionario = new Funcionario();
        TipodeIdentificacion tipoId = new TipodeIdentificacion(jCBTipoDocumento.getSelectedIndex() + 1);
        funcionario.setIdTipodeIdentificacion(tipoId);
        funcionario.setNumeroIdentificacion(jTFDocumento.getText().trim());
        funcionario.setNombre(jTFNombres.getText().trim());
        funcionario.setApellido(jTFApellidos.getText().trim());
        funcionario.setSexo(jCBSexo.getSelectedItem().toString());
        funcionario.setTelefono(jFFTelefono.getText());
        if(JDChFechaNac.getDate() != null){
            funcionario.setFechaNacimiento(JDChFechaNac.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        }else{
            funcionario.setFechaNacimiento(LocalDate.now(ZoneId.systemDefault()));
        }
        EstadoCivil estadoCivil = new EstadoCivil(JCBEstadoCivil.getSelectedIndex() + 1);
        funcionario.setIdEstadoCivil(estadoCivil);
        funcionario.setDireccion(jTADireccion.getText().trim());
        int resultado = funcionarioCtrl.guardar(funcionario);
        limpiar();
        return resultado > 0;


    }

    private boolean actualizar(){
        Funcionario funcionario = new Funcionario();


        TipodeIdentificacion tipoId = new TipodeIdentificacion(jCBTipoDocumento.getSelectedIndex() + 1);
        funcionario.setId(Integer.parseInt(jTblFuncionarios.getValueAt(jTblFuncionarios.getSelectedRow(), 0).toString()));
        funcionario.setIdTipodeIdentificacion(tipoId);
        funcionario.setNumeroIdentificacion(jTFDocumento.getText().trim());
        funcionario.setNombre(jTFNombres.getText().trim());
        funcionario.setApellido(jTFApellidos.getText().trim());
        funcionario.setSexo(jCBSexo.getSelectedItem().toString());
        funcionario.setTelefono(jFFTelefono.getText());
        if(JDChFechaNac.getDate() != null){
            funcionario.setFechaNacimiento(JDChFechaNac.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        }else{
            funcionario.setFechaNacimiento(LocalDate.now(ZoneId.systemDefault()));
        }
        EstadoCivil estadoCivil = new EstadoCivil(JCBEstadoCivil.getSelectedIndex() + 1);
        funcionario.setIdEstadoCivil(estadoCivil);
        funcionario.setDireccion(jTADireccion.getText().trim());
        int resultado = funcionarioCtrl.actualizar(funcionario);
        limpiar();
        return resultado > 0;
    }
